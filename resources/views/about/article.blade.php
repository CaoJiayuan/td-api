                <div class="maincon" name="con延期交收交易每日无负债结算制度？">
                    <p>对于延期交收合约，每日无负债结算制度又称“逐日盯市”制度，具体来说，就是在每日交易结束后，交易所按照合约当日的结算价对会员账户上所有合约的盈亏、保证金、手续费、延期费等费用进行结算，对应收应付款项实行净额一次划转，相应增加或减少会员的结算准备金，当结算准备金不足时，会员应在规定时间内补足保证金，否则，交易所有权对会员持有的合约进行强行平仓。</p>
                    <p>交易所会员按照相同的方式对其代理的客户进行结算，当客户保证金不足时，会员将要求客户追加保证金，客户应在约定时间内补足保证金，否则会员有权按照双方“交易代理协议”的约定对该客户持有的合约进行强行平仓。</p>
                    <p>例如，假设客户在开盘后以290 元/ 克的价格买入2 手Au(T+D), 保证金比例为15%，所需的保证金为87,000 元（290X1000X2X15%），若当日收盘后结算价为280 元/ 克，则当日盈亏(280-290）X1000X2=-20,000, 以结算价计算的合约保证金需84,000 元，客户需补充的保证金为84,000-(87,000-20,000)=17,000 元。（不计算手续费）</p>
                </div>
