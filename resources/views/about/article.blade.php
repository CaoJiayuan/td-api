            <li name="实物黄金交易与纸黄金有何区别？">
                <div class="title"><span class="problem">实物黄金交易与纸黄金有何区别？</span><span class="updown active"></span></div>
                <div class="maincon" name="con实物黄金交易与纸黄金有何区别？">
                    <p>一、交易对象不同</p>
                    <p> 个人投资者通过银行的代理参与交易所实物黄金的买卖，交易的对象是交易所标准化的黄金实物；</p>
                    <p>纸黄金的交易对象是代表黄金价格的虚拟的数字，买卖的价格是银行报出的价格。</p>
                    <p>二、交易对手不同</p>
                    <p>投资者买卖交易所实物黄金的交易对手为其他参与交易所实物黄金交易的投资者；</p>
                    <p>对纸黄金而言，所有投资者的交易对手都是银行。</p>
                    <p>三、交易机制不同</p>
                    <p>交易所的实物黄金交易采用场内竞价撮合模式，与股票交易模式完全相同。投资者能自主参与报价，根据价格优先、时间优先的原则由计算机撮合成交，交易系统实时公开买卖报价和成交价，买家和卖家直接交易；</p>
                    <p>纸黄金的交易中，投资者只能根据银行的报价点价成交，不能参与报价，买入价与卖出价之间有较大的价差。</p>
                    <p>四、实物提取不同</p>
                    <p>通过交易所交易的是实实在在的黄金实物，买入交易所的实物黄金后，投资者账户上的黄金是有存放在金库的实物黄金相对应的，投资者既可以在价格较高时卖出赚取差价，也可以从金库中提取实物，是一种投资、保值两相宜的产品；</p>
                    <p>纸黄金交易的是银行报出的黄金买卖价格，是一种账面上的黄金交易，不能提取实物，所以银行也把它叫做“账户金”。</p>
                    <p>五、交易成本不同</p>
                    <p>交易所的实物黄金交易的成本为交易手续费，交易所的手续费加上代理商的手续费一般不高于成交金额的万分之八；</p>
                    <p>纸黄金的交易成本不直接表现为交易手续费，而是表现为同一时点上的买卖差价，银行从买卖差价中获取收益，买卖价差如果折算为交易手续费率的话，其手续费率远远高于交易所的手续费率。</p>
