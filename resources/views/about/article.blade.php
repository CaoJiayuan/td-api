                    <p>延期交收合约的每日盈亏由当日持仓盈亏及当日平仓盈亏组成：</p>
                    <p> 一、当日持仓盈亏是客户的未平仓合约因每日结算价的变化而产生的，由持历史仓盈亏和持今日新仓盈亏组成，计算方法如下：</p>
                    <p>持历史仓盈亏=（当日结算价- 昨日结算价）X 持仓量X 合约单位</p>
                    <p>持今日新仓盈亏=（当日结算价- 当日开仓价）X 持仓量X 合约单位</p>
                    <p>二、当日平仓盈亏由平仓了结的合约产生的，也可以分为平历史仓盈亏和平今日仓盈亏</p>
                    <p>平历史仓盈亏=（当日平仓价- 昨日结算价）X 平仓量X 合约单位</p>
