                <div class="title"><span class="problem">延期交收合约与期货合约有何异同？</span><span class="updown active"></span></div>
                <div class="maincon" name="con延期交收合约与期货合约有何异同？">
                    <p>相同点：</p>
                    <p>1、交易的对象都是标准化的合约；</p>
                    <p>2、都是以保证金方式进行的交易；</p>
                    <p>3、都有开仓、平仓的交易指令及持仓的概念；</p>
                    <p>4、都有保证金制度、限仓制度、涨跌停板制度、大户报告制度、强行平仓制度等风险控制措施；</p>
                    <p>5、期货交易的当日无负债结算制度与延期交收合约交易的每日无负债结算制度也基本相同；</p>
                    <p>不同点：</p>
                    <p>1、延期交收合约交易没有固定的交割期</p>
                    <p>延期交收合约交易，每天买卖双方都可以自由地选择是否进行交收申报，没有固定的交割期，持仓时间也不受限制，这与期货合约中具有固定的交割期限有本质的不同。</p>
                    <p>2、延期交收合约交易以交割为基础</p>
                    <p>例如，通常情况下，黄金延期交收合约的交收比一般在50% 左右，以交割为基础的交易模式和特征非常明显，这与不以交割为目的的期货交易有显著的区别。</p>
                    <p>3、延期交收合约的交易价格为现货价格</p>
                    <p>延期交收合约交易具有明显的现货属性，因为每日都在进行交割，因此延期交收合约的价格不会背离现货价格，能直接反映市场的供求关系；期货的价格一般情况下与现货价格存在基差。</p>
                    <p>4、 延期交收合约交易具备特有的交易特性</p>
                    <p>延期交收合约交易具有延期补偿费制度、中立仓制度、超期费制度等期货交易没有的特有制度。</p>
                    <p>5、 延期交收合约交易的风险控制机制更丰富</p>
                    <p>延期交收合约交易除了具有与期货交易相同的保证金制度、限仓制度、涨跌停板制度、强行平仓制度等风险控制制度外，其特有的延期补偿费制度、中立仓制度、超期费制度等也为交易风险的控制提供了更丰富的手段。</p>
                    <p>另外，延期交收合约没有固定交割日，不强制交割，每天都进行的交割实际上使得风险不易累积，无期货中可能存在的逼仓风险。</p>
