                    <p>在延期交收合约交易中，只需按合约价值的一定比率交纳少量资金作为履行合约的资金担保，便可参与合约的买卖，这种资金就是保证金。</p>
                    <p>新开仓时所需交纳的保证金是根据合约金额和保证金比率确定的，即：保证金＝合约金额X 保证金比率。</p>
                    <p>交易所Au(T+D) 合约的最低保证金比率为交易金额的11%（交易所可以根据长假或其他市场风险情况调整保证金比例），会员单位会在交易所保证金的基础上加收一定比例的保证金。</p>
                    <p>例如，某客户以300 元/ 克的价格卖出2 手Au(T+D) 合约（每手1000 克），会员可按照15% 的保证金比例， 那么， 他必须支付的保证金为90，000 元（ 即300x2000x15％）。在持仓过程中，会因市场行情的不断变化而产生浮动盈亏。清算时，浮动盈亏将成为实际盈亏，同时，还须根据结算价重新计算持有合约的保证金。</p>
                    <p>保证金对于保障市场的正常运转具有重要作用。</p>
                    <p>（1）保证金制度的实施，降低了交易成本，使用一定比例的保证金就可从事合约的交易，发挥了资金杠杆作用，促进套期保值功能的发挥。</p>
                    <p>（2）保证金为合约的履行提供资金担保。保证金制度可以保证所有账户的每一笔交易和持有的每一个头寸都具有与其面临风险相适应的资金，交易中发生的盈亏不断得到相应的处理，杜绝了负债现象。因此这一制度的严格执行为合约的履行提供了安全可靠的保障。</p>
                    <p>（3）保证金是交易所调整交易规模的重要于段。当市场火爆、交易规模过大时，可通过提高保证金的办法，增加交易成本，以抑制投机行为，控制交易的规模和风险。反之，当市场低迷、交易规模过小时，则可通过适量降低保证金来吸引更多的市场参与者，活跃交易气氛。</p>
                </div>
