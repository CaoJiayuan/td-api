                    <p>结算单一般包括户名、成交日期、成交品种、成交数量及结算价格、买入或卖出、交易手续费和其他费用等内容。</p>
                    <p>由于延期交收合约是保证金交易，实行每日无负债结算制度，计算相对复杂，举例如下：</p>
                    <p>某客户开户后在保证金账户存入资金30 万元，某日开仓买入Au(T+D) 合约5 手，成交价280 元/ 克，同一天平仓2 手，成交价282 元/ 克，当日结算价281 元/ 克，假设保证金比例为15%，手续费率为万分之四，则该客户当日的交易盈亏及结算情况如下：</p>
                    <p>当日平仓盈亏=（282-280）X1000 X2=4000 元</p>
                    <p>当日持仓盈亏=（281-280）X1000 X3=3000 元</p>
                    <p>当日盈亏=4000+3000=7000 元</p>
                    <p>手续费=280X1000X5X4/10000+282X1000X2X4/10000=560+225.6=785.6 元</p>
                    <p>当日权益=300000+7000-785.6=306214.4 元</p>
                    <p>保证金占用=281X1000X3X15%=126450 元</p>
                    <p>可用资金=306214.4-126450=179764.4 元</p>
